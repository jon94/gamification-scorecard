function renderOrgStatus() {
  const bar = document.getElementById('org-status-bar');
  if (!bar) return;
  bar.innerHTML = '';

  const m = DATA.milestones;
  const anyUser = DATA.users[0]; // org milestones are identical for all users

  // Helper: build one chip
  function makeChip(ms) {
    const passed = anyUser ? !!(anyUser.milestones || {})[ms.id] : false;
    const chip = document.createElement('button');
    chip.className = 'org-chip ' + (passed ? 'org-chip-pass' : 'org-chip-fail');
    chip.title = 'Click to see evidence';
    chip.innerHTML = `${ms.icon} ${ms.name} <span class="org-chip-status">${passed ? '✅' : '❌'}</span>`;
    chip.addEventListener('click', () => {
      if (anyUser) showEvidenceModal(anyUser, ms);
    });
    return chip;
  }

  // Org-level section
  const orgLabel = document.createElement('span');
  orgLabel.className = 'org-bar-label';
  orgLabel.textContent = 'Org Foundation';
  bar.appendChild(orgLabel);

  m.org.forEach(ms => bar.appendChild(makeChip(ms)));

  const sep = document.createElement('span');
  sep.className = 'org-bar-sep';
  bar.appendChild(sep);

  // Special achievements section
  const spLabel = document.createElement('span');
  spLabel.className = 'org-bar-label';
  spLabel.textContent = 'Special Achievements';
  bar.appendChild(spLabel);

  m.special.forEach(ms => bar.appendChild(makeChip(ms)));
}

