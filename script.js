function positionTooltip(e) {
  const tip = document.getElementById('tooltip');
  const x = e.clientX + 14;
  const y = e.clientY + 14;
  const maxX = window.innerWidth - 240;
  const maxY = window.innerHeight - 120;
  tip.style.left = Math.min(x, maxX) + 'px';
  tip.style.top  = Math.min(y, maxY) + 'px';
}

function hideTooltip() {
  document.getElementById('tooltip').classList.remove('visible');
}

function typeLabel(type) {
  switch (type) {
    case 'audit':  return 'Auto · Audit Trail';
    case 'metric': return 'Auto · Usage Metric';
    case 'manual': return 'Manual · overrides.json';
    case 'special':return 'Auto · Special Achievement';
    default: return type;
  }
}

