// Poll server for data; once received, render
function bootFromServer() {
  showLoadingState('Fetching live data from Datadog…');
  setFetchIndicator(true);
  fetchServerData();
  // Auto-poll server status to keep "last updated" fresh
  setInterval(fetchServerData, 30 * 1000); // re-fetch every 30 sec — keeps all viewers in sync
}

function fetchServerData() {
  fetch('/api/data')
    .then(r => r.json())
    .then(json => {
      if (json.status === 'refreshing') {
        showLoadingState('First data fetch in progress… retrying in 5s');
        setTimeout(fetchServerData, 5000);
        return;
      }
      DATA      = json;
      IS_SAMPLE = false;
      setFetchIndicator(false);
      // In server mode, overrides.json is the source of truth — no localStorage needed
      renderAll();
    })
    .catch(err => {
      console.warn('Server fetch failed:', err);
      fallbackToStatic();
    });
}

// ─── localStorage helpers for static/tiiny.host mode ────────────────────────
const LS_KEY = 'dd_scorecard_manual_overrides';

function loadLocalOverrides() {
  try { return JSON.parse(localStorage.getItem(LS_KEY) || '{}'); } catch (_) { return {}; }
}

function saveLocalOverride(email, milestoneId, value) {
  try {
    const stored = loadLocalOverrides();
    if (!stored[email]) stored[email] = {};
    stored[email][milestoneId] = value;
    localStorage.setItem(LS_KEY, JSON.stringify(stored));
  } catch (_) { /* storage full or unavailable */ }
}

function applyLocalOverridesToData(data) {
  if (!data || !data.users) return;
  const stored = loadLocalOverrides();
  for (const [email, milestones] of Object.entries(stored)) {
    const user = data.users.find(u => u.email === email);
    if (!user) continue;
    for (const [msId, val] of Object.entries(milestones)) {
      user.milestones[msId] = val;
    }
  }
}

function bootFromStatic() {
  if (window.DD_DATA && window.DD_DATA.users) {
    DATA      = window.DD_DATA;
    IS_SAMPLE = false;
  } else if (window.DD_SAMPLE_DATA && window.DD_SAMPLE_DATA.users) {
    DATA      = window.DD_SAMPLE_DATA;
    IS_SAMPLE = true;
  }
  if (!DATA) {
    document.body.innerHTML = '<div style="padding:40px;color:#f66;font-family:monospace">No data found. Run <code>node refresh.js</code> or <code>node server.js</code>.</div>';
    return;
  }
  // Restore any manual overrides saved in localStorage (static/tiiny.host mode)
  if (!IS_LOCAL_SERVER) applyLocalOverridesToData(DATA);
  renderAll();
}

function fallbackToStatic() {
  DATA      = window.DD_DATA?.users ? window.DD_DATA : window.DD_SAMPLE_DATA;
  IS_SAMPLE = !window.DD_DATA?.users;
  if (DATA) renderAll();
}

