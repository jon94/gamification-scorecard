function makeCertifiedCard(users) {
  const card = document.createElement('div');
  card.className = 'score-card';
  card.innerHTML = `<div class="score-card-title">🎓 Certified Practitioners</div>`;

  const certified = users.filter(u => u.certification);

  if (certified.length === 0) {
    card.innerHTML += `<div class="empty-state">No certified practitioners yet.</div>`;
    return card;
  }

  // Group by tier (highest first)
  const tierOrder = ['platinum', 'gold', 'silver', 'bronze'];
  tierOrder.forEach(tid => {
    const group = certified.filter(u => u.certification === tid);
    if (group.length === 0) return;

    const t = DATA.certification_tiers.find(t => t.id === tid);
    group.forEach(user => {
      const row = document.createElement('div');
      row.className = 'cert-practitioner';
      row.innerHTML = `
        <span class="cp-badge">${t.icon}</span>
        <div class="cp-avatar" style="
          width:28px;height:28px;border-radius:50%;background:${stringColor(user.email)};
          display:inline-flex;align-items:center;justify-content:center;
          font-size:10px;font-weight:700;color:#fff;flex-shrink:0;margin-right:4px
        ">${initials(user.name)}</div>
        <span class="cp-name">${user.name}</span>
        <span class="cp-level ${tid}">${t.name}</span>
      `;
      card.appendChild(row);
    });
  });

  return card;
}

