function refreshGridBody() {
  const wrap = document.getElementById('fg-wrap');
  if (!wrap) return;
  wrap.innerHTML = '';

  const m = DATA.milestones;
  const users = getSortedFilteredUsers();

  // Milestone columns: core, day1, day2, self_learning, advance (with separators between)
  const colSections = [
    { items: m.core,          cls: 'fg-col-core',          label: 'CORE — Audit Trail' },
    { items: m.day1,          cls: 'fg-col-day1',          label: 'HKJC Day 1' },
    { items: m.day2,          cls: 'fg-col-day2',          label: 'HKJC Day 2' },
    { items: m.self_learning, cls: 'fg-col-self-learning', label: 'Self Learning' },
    { items: m.advance,       cls: 'fg-col-advance',       label: 'ADVANCED — Certifications' },
  ];
  const allCols = colSections.flatMap((s, si) =>
    s.items.map((ms, mi) => ({ ms, cls: s.cls, firstInSection: mi === 0, sectionLabel: s.label, isLast: si === colSections.length - 1 }))
  );

  const table = document.createElement('table');
  table.className = 'fg-table';

  // === THEAD ===
  const thead = document.createElement('thead');

  // Section label row
  const labelRow = document.createElement('tr');
  const cornerBlank = document.createElement('th');
  cornerBlank.className = 'fg-corner fg-sticky-col';
  cornerBlank.rowSpan = 2;
  cornerBlank.innerHTML = `<span class="fg-champion-count">${users.length} champion${users.length !== 1 ? 's' : ''}</span>`;
  labelRow.appendChild(cornerBlank);

  colSections.forEach((s, si) => {
    const th = document.createElement('th');
    th.colSpan = s.items.length;
    th.className = 'fg-section-label ' + s.cls;
    th.textContent = s.label;
    labelRow.appendChild(th);
    if (si < colSections.length - 1) {
      const sep = document.createElement('th');
      sep.className = 'fg-col-sep-header';
      sep.rowSpan = 2;
      labelRow.appendChild(sep);
    }
  });

  const progTh = document.createElement('th');
  progTh.className = 'fg-progress-hdr';
  progTh.rowSpan = 2;
  progTh.textContent = 'Progress';
  labelRow.appendChild(progTh);

  const gcTh = document.createElement('th');
  gcTh.className = 'fg-gc-hdr';
  gcTh.rowSpan = 2;
  gcTh.textContent = 'GC Earned';
  labelRow.appendChild(gcTh);

  thead.appendChild(labelRow);

  // Milestone icon row
  const msRow = document.createElement('tr');
  allCols.forEach(({ ms, cls, firstInSection, isLast }) => {
    const th = document.createElement('th');
    th.className = 'fg-ms-hdr ' + cls;
    th.title = ms.name + ' — ' + ms.points + ' GC\n' + (ms.audit_query || 'Manual override');
    th.textContent = ms.icon;
    msRow.appendChild(th);

    if (!isLast && allCols[allCols.indexOf({ ms, cls, firstInSection, isLast })]?.firstInSection === false) {
      // separators already added in label row
    }
  });
  thead.appendChild(msRow);
  table.appendChild(thead);

  // === TBODY ===
  const tbody = document.createElement('tbody');

  if (users.length === 0) {
    const emptyRow = document.createElement('tr');
    const emptyTd = document.createElement('td');
    emptyTd.colSpan = allCols.length + 4;
    emptyTd.className = 'fg-empty';
    emptyTd.textContent = 'No champions match the current filter.';
    emptyRow.appendChild(emptyTd);
    tbody.appendChild(emptyRow);
  } else {
    users.forEach((user, idx) => {
      tbody.appendChild(buildFGRow(user, idx + 1, allCols, colSections));
    });
  }

  table.appendChild(tbody);
  wrap.appendChild(table);
}

function buildFGRow(user, rank, allCols, colSections) {
  const pct = completionPct(user);
  const c   = certLabel(user.certification);
  const row = document.createElement('tr');
  row.className = 'fg-user-row';
  row.dataset.email = user.email;

  // ── User info cell (sticky) ──
  const userTd = document.createElement('td');
  userTd.className = 'fg-user-cell fg-sticky-col';
  userTd.innerHTML = `
    <div class="fg-user-inner">
      <div class="fg-avatar" style="background:${stringColor(user.email)}">${initials(user.name)}</div>
      <div class="fg-name-wrap">
        <div class="fg-name">${user.name}</div>
        <div class="fg-cert-label" style="color:${c.color}">${c.icon} ${c.name}</div>
      </div>
    </div>`;
  row.appendChild(userTd);

  // ── Milestone cells ──
  let secIdx = 0;
  allCols.forEach(({ ms, cls, firstInSection }, colIdx) => {
    // Section separator column
    if (firstInSection && colIdx > 0) {
      const sepTd = document.createElement('td');
      sepTd.className = 'fg-col-sep';
      row.appendChild(sepTd);
    }

    const done  = !!(user.milestones || {})[ms.id];
    const td    = document.createElement('td');
    td.className = 'fg-cell ' + cls + (done ? ' fg-done' : ' fg-todo');

    if (done) {
      td.textContent = '✓';
    } else if (ms.type === 'manual') {
      td.textContent = '○';
      if (isAdminMode) {
        td.classList.add('fg-manual');
        td.title = 'Click to mark done — ' + ms.name;
        td.addEventListener('click', () => {
          user.milestones[ms.id] = true;
          td.textContent = '✓';
          td.classList.replace('fg-todo', 'fg-done');
          td.classList.remove('fg-manual');
          recalculateUser(user);
          updateFGRowStats(row, user);
          renderScoreboard();
          if (IS_LOCAL_SERVER) {
            fetch('/api/manual-override', { method: 'POST', headers: {'Content-Type':'application/json'},
              body: JSON.stringify({ email: user.email, milestone_id: ms.id, value: true }) }).catch(() => {});
          } else {
            saveLocalOverride(user.email, ms.id, true);
          }
        });
      } else {
        td.title = ms.name + ' — unlock admin mode to edit';
      }
    } else {
      td.innerHTML = '<span class="fg-lock">🔒</span>';
    }

    // Click for evidence modal (non-manual: single click; manual: shift+click)
    td.addEventListener('click', e => {
      if (ms.type !== 'manual' || e.shiftKey) showEvidenceModal(user, ms);
    });
    td.addEventListener('contextmenu', e => { e.preventDefault(); showEvidenceModal(user, ms); });

    row.appendChild(td);
  });

  // ── Progress cell ──
  const progTd = document.createElement('td');
  progTd.className = 'fg-progress-cell';
  progTd.innerHTML = `
    <div class="fg-bar-bg"><div class="fg-bar-fill" style="width:${pct}%"></div></div>
    <div class="fg-pct">${pct}%</div>`;
  row.appendChild(progTd);

  // ── GC cell ──
  const gcTd = document.createElement('td');
  gcTd.className = 'fg-gc-cell';
  gcTd.textContent = user.total_gc.toLocaleString();
  row.appendChild(gcTd);

  return row;
}

function updateFGRowStats(row, user) {
  const pct  = completionPct(user);
  const c    = certLabel(user.certification);
  const fill = row.querySelector('.fg-bar-fill');
  const pctEl = row.querySelector('.fg-pct');
  const gcEl  = row.querySelector('.fg-gc-cell');
  const certEl = row.querySelector('.fg-cert-label');
  if (fill)   fill.style.width = pct + '%';
  if (pctEl)  pctEl.textContent = pct + '%';
  if (gcEl)   gcEl.textContent  = user.total_gc.toLocaleString();
  if (certEl) { certEl.textContent = c.icon + ' ' + c.name; certEl.style.color = c.color; }
}

function getSortedFilteredUsers() {
  let users = [...DATA.users];
  if (gridFilter !== 'all') users = users.filter(u => (u.certification || 'none') === gridFilter);
  if (gridSearch) {
    const q = gridSearch.toLowerCase();
    users = users.filter(u => u.name.toLowerCase().includes(q) || u.email.toLowerCase().includes(q));
  }
  if (gridSort === 'gc')   users.sort((a, b) => b.total_gc - a.total_gc);
  if (gridSort === 'pct')  users.sort((a, b) => completionPct(b) - completionPct(a));
  if (gridSort === 'name') users.sort((a, b) => a.name.localeCompare(b.name));
  return users;
}

// ─── Toggle manual cell (kept for backward compat; new grid handles inline) ──
function toggleManualCell(user, ms, td) {
  const newVal = !user.milestones[ms.id];
  user.milestones[ms.id] = newVal;
  td.classList.toggle('done', newVal);
  td.classList.toggle('not-done', !newVal);
  td.textContent = newVal ? '✓' : '';
  recalculateUser(user);
  renderScoreboard();
  // Update row stats in new flipped grid
  const row = document.querySelector(`.fg-user-row[data-email="${user.email}"]`);
  if (row) updateFGRowStats(row, user);
}

function recalculateUser(user) {
  // Org milestones excluded — individual scoring only
  const allMs = INDIVIDUAL_SECTIONS.flatMap(s => DATA.milestones[s] || []);

  let gc = 0;
  allMs.forEach(ms => {
    if (user.milestones[ms.id]) gc += ms.points;
  });

  // Bonus GC from certification
  let cert = null;
  let bonus = 0;
  const tiers = [...DATA.certification_tiers].reverse(); // check from highest

  for (const tier of tiers) {
    const hasAll = tier.required.every(id => user.milestones[id]);
    if (hasAll) {
      cert = tier.id;
      bonus = tier.gc_bonus;
      break;
    }
  }

  user.certification  = cert;
  user.milestone_gc   = gc;
  user.bonus_gc       = bonus;
  user.total_gc       = gc + bonus;
}

function refreshUserCertBadge(user) {
  const row = document.querySelector(`.fg-user-row[data-email="${user.email}"]`);
  if (row) updateFGRowStats(row, user);
  // Legacy no-op kept so old call sites don't break
  const idx = DATA.users.indexOf(user);
  if (idx < 0) return;
  const badges = document.querySelectorAll('.user-cert-badge');
  if (badges[idx]) {
    const c = certLabel(user.certification);
    badges[idx].textContent = c.icon;
    badges[idx].title = c.name + ' — ' + user.total_gc + ' GC';
  }
}

