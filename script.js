// ─── Utilities ───────────────────────────────────────────────────────────────
function formatDate(iso) {
  if (!iso) return '—';
  try {
    const d = new Date(iso);
    return d.toLocaleString('en-HK', { dateStyle: 'medium', timeStyle: 'short' });
  } catch { return iso; }
}

/** Stable color from string hash */
function stringColor(str) {
  const PALETTE = [
    '#e53e3e','#dd6b20','#d69e2e','#38a169','#319795',
    '#3182ce','#6b46c1','#d53f8c','#2b6cb0','#285e61',
    '#744210','#276749','#1a365d','#44337a','#702459'
  ];
  let h = 0;
  for (let i = 0; i < str.length; i++) h = (h * 31 + str.charCodeAt(i)) >>> 0;
  return PALETTE[h % PALETTE.length];
}

/** Two-letter initials from name */
function initials(name) {
  const parts = name.trim().split(/\s+/);
  if (parts.length >= 2) return (parts[0][0] + parts[parts.length - 1][0]).toUpperCase();
  return name.slice(0, 2).toUpperCase();
}

/** Total milestone count across all sections */
// Org milestones are org-wide context only — excluded from individual scoring
const INDIVIDUAL_SECTIONS = ['core', 'day1', 'day2', 'self_learning', 'advance'];

function totalMilestoneCount() {
  const m = DATA.milestones;
  return INDIVIDUAL_SECTIONS.reduce((n, s) => n + (m[s]?.length || 0), 0);
}

/** Count completed individual milestones for a user (excludes org) */
function completedCount(user) {
  const m = DATA.milestones;
  return INDIVIDUAL_SECTIONS.flatMap(s => m[s] || [])
    .filter(ms => user.milestones[ms.id]).length;
}

/** Completion percentage for a user */
function completionPct(user) {
  return Math.round((completedCount(user) / totalMilestoneCount()) * 100);
}

/** Certification tier label/icon for a user */
function certLabel(certId) {
  const tier = DATA.certification_tiers.find(t => t.id === certId);
  return tier ? { icon: tier.icon, name: tier.name, color: tier.color } : { icon: '—', name: 'None', color: '#555' };
}

// ─── Avatar element ──────────────────────────────────────────────────────────
function makeAvatar(user, size = 42) {
  const el = document.createElement('div');
  el.className = 'user-avatar';
  el.style.width  = size + 'px';
  el.style.height = size + 'px';
  el.style.fontSize = Math.round(size * 0.33) + 'px';
  el.style.background = stringColor(user.email);
  el.textContent = initials(user.name);
  return el;
}

