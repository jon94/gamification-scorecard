'use strict';

// ─── Detect mode ────────────────────────────────────────────────────────────
// Use server API for any http(s):// origin (localhost dev OR production GCP)
// Only use static/fallback mode for file:// (direct file open without a server)
const IS_LOCAL_SERVER = (
  typeof location !== 'undefined' &&
  /^https?:\/\//.test(location.origin)
);

let DATA      = null;
let IS_SAMPLE = false;

// ─── Admin PIN state ─────────────────────────────────────────────────────────
let isAdminMode = sessionStorage.getItem('adminMode') === 'true';

function toggleAdminLock() {
  if (isAdminMode) {
    isAdminMode = false;
    sessionStorage.removeItem('adminMode');
    updateLockIcon();
    refreshGridBody();
  } else {
    openPinModal();
  }
}

function openPinModal() {
  document.getElementById('pin-backdrop').classList.add('open');
  document.getElementById('pin-modal').classList.add('open');
  document.getElementById('pin-error').textContent = '';
  document.getElementById('pin-input').value = '';
  setTimeout(() => document.getElementById('pin-input').focus(), 50);
}

function closePinModal() {
  document.getElementById('pin-backdrop').classList.remove('open');
  document.getElementById('pin-modal').classList.remove('open');
}

function submitPin() {
  const pin = document.getElementById('pin-input').value.trim();
  if (!pin) return;
  fetch('/api/verify-pin', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ pin })
  })
  .then(r => r.json())
  .then(({ valid }) => {
    if (valid) {
      isAdminMode = true;
      sessionStorage.setItem('adminMode', 'true');
      closePinModal();
      updateLockIcon();
      refreshGridBody();
    } else {
      document.getElementById('pin-error').textContent = 'Incorrect PIN — try again';
      document.getElementById('pin-input').value = '';
      document.getElementById('pin-input').focus();
    }
  })
  .catch(() => {
    document.getElementById('pin-error').textContent = 'Could not verify PIN';
  });
}

function updateLockIcon() {
  const btn = document.getElementById('btn-admin-lock');
  if (!btn) return;
  if (isAdminMode) {
    btn.textContent = '🔓';
    btn.title = 'Admin mode ON — click to lock';
    btn.classList.add('unlocked');
  } else {
    btn.textContent = '🔒';
    btn.title = 'Admin mode — click to unlock circle editing';
    btn.classList.remove('unlocked');
  }
}

